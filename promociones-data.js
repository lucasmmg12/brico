// === FUNCI칍N PARA OBTENER PROMOCI칍N POR ID ===
export function obtenerPromocionPorId(id) {
    const todas = obtenerTodasLasPromociones();
    return todas.find(promo => promo.id === id);
}

// === FUNCI칍N PARA OBTENER PROMOCIONES POR TIPO ===
